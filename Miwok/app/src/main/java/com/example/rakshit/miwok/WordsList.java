package com.example.rakshit.miwok;

public class WordsList {

    private String miwok;
    private String eng;

    public WordsList(String miwok, String eng) {
        this.miwok = miwok;
        this.eng = eng;
    }

    public String getmiwok() {
        return miwok;
    }

    public String geteng() { return eng; }
}
